#include "libft.h"

void		ft_striter(char *s, void (*f)(char *))
{
	int		i;
	char	*ptr;

	if (!s || !f)
		return ;
	i = 0;
	ptr = s;
	while (ptr[i])
		f(&ptr[i++]);
}
