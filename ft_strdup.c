#include "libft.h"

char		*ft_strdup(const char *s1)
{
	int		i;
	char	*new;

	i = 0;
	if (!(new = (char *)malloc(sizeof(char) * ft_strlen(s1) + 1)))
		return (NULL);
	while (s1[i] != '\0')
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
